function createCategoryTabs() {
  const labels = i18n[state.lang].categories;
  categoryContainer.innerHTML = categoryKeys
    .map(
      (category) => `
        <button
          type="button"
          role="tab"
          aria-selected="${category === state.category}"
          class="${category === state.category ? "is-active" : ""}"
          data-category="${category}"
        >
          ${labels[category]}
        </button>
      `
    )
    .join("");
}

function getFilteredItems() {
  const query = state.search.trim().toLowerCase();
  const labels = i18n[state.lang].categories;

  return menuItems.filter((item) => {
    const localized = item[state.lang];
    const inCategory = state.category === "all" || item.category === state.category;
    const searchableText = [
      localized.name,
      localized.price,
      localized.description,
      labels[item.category],
      ...localized.tags
    ]
      .join(" ")
      .toLowerCase();
    return inCategory && (!query || searchableText.includes(query));
  });
}

function renderMenu() {
  const items = getFilteredItems();
  const copy = i18n[state.lang].menu;

  menuContainer.innerHTML = items
    .map((item) => {
      const localized = item[state.lang];
      const cardClass = `menu-card ${item.featured ? "is-featured" : ""}`;
      const detailPath = typeof item.detailPath === "string" ? item.detailPath : item.detailPath?.[state.lang];
      const cardTag = detailPath ? "a" : "article";
      const cardAttributes = detailPath
        ? `class="${cardClass}" href="${detailPath}" aria-label="${copy.detail} ${localized.name}"`
        : `class="${cardClass}"`;

      return `
        <${cardTag} ${cardAttributes}>
          <div class="menu-card-header">
            <h3>${localized.name}</h3>
            <span class="price">${localized.price}</span>
          </div>
          <p>${localized.description}</p>
          ${
            item.image
              ? `<img class="menu-card-photo" src="${item.image.src}" alt="${item.image.alt[state.lang]}" loading="lazy" />`
              : ""
          }
          <div class="tag-row" aria-label="${copy.itemOptions} ${localized.name}">
            ${localized.tags
              .map((tag, index) => {
                const className = tag === "Pikantní" || tag === "Spicy" ? "tag spicy" : index % 2 ? "tag alt" : "tag";
                return `<span class="${className}">${tag}</span>`;
              })
              .join("")}
          </div>
          ${detailPath ? `<span class="menu-card-link">${copy.detail}</span>` : ""}
        </${cardTag}>
      `;
    })
    .join("");

  emptyState.hidden = items.length > 0;
}

function renderTodayHours() {
  const day = new Date().getDay();
  const copy = i18n[state.lang].hero;
  todayHours.textContent = day === 0 ? copy.openSunday : copy.openRegular;
}

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("the-ba-language", lang);
  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
  translateStaticCopy();
  createCategoryTabs();
  renderMenu();
  renderTodayHours();
}

function setCategory(category) {
  state.category = category;
  createCategoryTabs();
  renderMenu();
}

categoryContainer.addEventListener("click", (event) => {
  const button = event.target.closest("[data-category]");
  if (!button) return;
  setCategory(button.dataset.category);
});

searchInput.addEventListener("input", (event) => {
  state.search = event.target.value;
  renderMenu();
});

langSwitch.addEventListener("click", (event) => {
  const button = event.target.closest("[data-lang]");
  if (!button) return;
  setLanguage(button.dataset.lang);
});

navToggle.addEventListener("click", () => {
  const isOpen = nav.classList.toggle("is-open");
  document.body.classList.toggle("nav-open", isOpen);
  navToggle.setAttribute("aria-expanded", String(isOpen));
});

nav.addEventListener("click", () => {
  nav.classList.remove("is-open");
  document.body.classList.remove("nav-open");
  navToggle.setAttribute("aria-expanded", "false");
});

setLanguage(state.lang);
