function createCategoryTabs() {
  const labels = i18n[state.lang].categories;
  categoryContainer.innerHTML = categoryKeys
    .map(
      (category) => `
        <button
          type="button"
          role="tab"
          aria-selected="${category === state.category}"
          class="${category === state.category ? "is-active" : ""}"
          data-category="${category}"
        >
          ${labels[category]}
        </button>
      `
    )
    .join("");
}

function getFilteredItems() {
  const query = state.search.trim().toLowerCase();
  const labels = i18n[state.lang].categories;

  return menuItems.filter((item) => {
    const localized = item[state.lang];
    const inCategory = state.category === "all" || item.category === state.category;
    const searchableText = [
      localized.name,
      localized.price,
      localized.description,
      labels[item.category],
      ...localized.tags
    ]
      .join(" ")
      .toLowerCase();
    return inCategory && (!query || searchableText.includes(query));
  });
}

function renderMenu() {
  const items = getFilteredItems();
  const copy = i18n[state.lang].menu;

  menuContainer.innerHTML = items
    .map((item) => {
      const localized = item[state.lang];
      const cardClass = ["menu-card", item.image ? "has-photo" : "", item.featured ? "is-featured" : ""].filter(Boolean).join(" ");
      const detailPath = typeof item.detailPath === "string" ? item.detailPath : item.detailPath?.[state.lang];
      const cardTag = detailPath ? "a" : "article";
      const cardAttributes = detailPath
        ? `class="${cardClass}" href="${detailPath}" aria-label="${copy.detail} ${localized.name}"`
        : `class="${cardClass}"`;

      return `
        <${cardTag} ${cardAttributes}>
          <div class="menu-card-header">
            <h3>${localized.name}</h3>
            <span class="price">${localized.price}</span>
          </div>
          <p>${localized.description}</p>
          ${
            item.image
              ? `<img class="menu-card-photo" src="${item.image.src}" alt="${item.image.alt[state.lang]}" loading="lazy" />`
              : ""
          }
          <div class="tag-row" aria-label="${copy.itemOptions} ${localized.name}">
            ${localized.tags
              .map((tag, index) => {
                const className = tag === "Pikantní" || tag === "Spicy" ? "tag spicy" : index % 2 ? "tag alt" : "tag";
                return `<span class="${className}">${tag}</span>`;
              })
              .join("")}
          </div>
          ${detailPath ? `<span class="menu-card-link">${copy.detail}</span>` : ""}
        </${cardTag}>
      `;
    })
    .join("");

  emptyState.hidden = items.length > 0;
}

function renderTodayHours() {
  const day = new Date().getDay();
  const copy = i18n[state.lang].hero;
  todayHours.textContent = day === 0 ? copy.openSunday : copy.openRegular;
}

function getActiveMenuSlides() {
  const isSmallScreen = window.matchMedia("(max-width: 740px)").matches;
  return isSmallScreen ? menuSlides.mobile : menuSlides.desktop;
}

function updateMenuSlide() {
  if (!menuSlideImage || !menuCounter) return;
  const slides = getActiveMenuSlides();
  state.menuSlide = Math.min(Math.max(state.menuSlide, 0), slides.length - 1);
  menuSlideImage.src = slides[state.menuSlide];
  menuSlideImage.alt = `${i18n[state.lang].popup.menuAlt} ${state.menuSlide + 1}`;
  menuCounter.textContent = `${state.menuSlide + 1} / ${slides.length}`;
}

function showPopupPanel(panelName) {
  popupPanels.forEach((panel) => {
    panel.hidden = panel.dataset.popupPanel !== panelName;
  });

  welcomeModal?.classList.toggle("is-menu-view", panelName === "menu");

  if (panelName === "menu") {
    updateMenuSlide();
  }
}

function rememberWelcomeSeen() {
  try {
    sessionStorage.setItem(welcomeSeenKey, "true");
  } catch (error) {
    return false;
  }
  return true;
}

function wasWelcomeSeen() {
  try {
    return sessionStorage.getItem(welcomeSeenKey) === "true";
  } catch (error) {
    return false;
  }
}

function openWelcomeModal(panelName = "intro") {
  if (!welcomeModal) return;
  window.clearTimeout(welcomeCloseTimer);
  rememberWelcomeSeen();
  showPopupPanel(panelName);
  welcomeModal.hidden = false;
  welcomeModal.classList.remove("is-closing");
  document.body.classList.add("modal-open");

  window.requestAnimationFrame(() => {
    welcomeModal.classList.add("is-open");
    const focusTarget = panelName === "menu" ? menuNextButton : welcomeModal.querySelector(".welcome-dialog");
    if (focusTarget) focusTarget.focus({ preventScroll: true });
  });
}

function closeWelcomeModal() {
  if (!welcomeModal) return;
  window.clearTimeout(welcomeCloseTimer);
  welcomeModal.classList.remove("is-open");
  welcomeModal.classList.add("is-closing");
  document.body.classList.remove("modal-open");
  welcomeCloseTimer = window.setTimeout(() => {
    welcomeModal.hidden = true;
    welcomeModal.classList.remove("is-closing");
  }, welcomeAnimationMs);
}

function moveMenuSlide(direction) {
  const slides = getActiveMenuSlides();
  state.menuSlide = (state.menuSlide + direction + slides.length) % slides.length;
  updateMenuSlide();
}

function setLanguage(lang) {
  state.lang = lang;
  localStorage.setItem("the-ba-language", lang);
  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
  translateStaticCopy();
  createCategoryTabs();
  renderMenu();
  renderTodayHours();
}

function setCategory(category) {
  state.category = category;
  createCategoryTabs();
  renderMenu();
}

